const translations: Record<EffectiveLanguage, TranslationMap> = { en, zh };

export function createTranslator(language: EffectiveLanguage) {
  return (key: TranslationKey) => translations[language][key];
}

export function resolveEffectiveLanguage(preference: AppLanguagePreference, systemLanguage = navigator.language): EffectiveLanguage {
  if (preference === "zh" || preference === "en") {
    return preference;
  }

  return systemLanguage.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function oppositeLanguage(language: EffectiveLanguage): Exclude<AppLanguagePreference, "system"> {
  return language === "zh" ? "en" : "zh";
}
